#' Location data for Aracari pluricinctus
#'
#' A dataset containing the xy locations for several Aracari pluricinctus individuals. Data collected by Kimberly Holbrook. The original location datasets have been formatted using the package adehabitatLT
#'
#' @format A data frame with 907 rows and 14 variables:
#' \describe{
#' \item{x}{x coordinate}
#' \item{y}{y coordinate}
#' \item{date}{date}
#' \item{dx}{change in x coordinate from previous consecutive location}
#' \item{dy}{change in y coordinate from previous consecutive location}
#' \item{dist}{distance in meters from previous location}
#' \item{dt}{time in seconds from previous location}
#' \item{R2n}{net squared displacement as defined in the adehabitatLT package}
#' \item{abs.angle}{absolute angle in radians}
#' \item{rel.angle}{relative angle in radians}
#' \item{id}{tag id for each individual bird}
#' \item{burst}{burst of relocations}
#' \item{pkey}{unique identifier of burst and individual}
#' \item{sgroup}{social group to which the animal belongs. NA means no information available}
#' ...
#' }
#'
#'
#'
#'
#'
#' @source \url{https://onlinelibrary.wiley.com/doi/full/10.1111/j.1744-7429.2010.00710.x}
